class Library:
  def __init__(self):
    self.books = []

  def add_book(self, book):
    self.books.append(book)

  def display_books(self):
    for book in self.books:
      print(f"Title: {book.title}")
      print(f"Author: {book.author}")
      print(f"Published Year: {book.published_year}")
      print(f"Available: {book.available}")
      print("-" * 20)

  def borrow_book(self, title):
    for book in self.books:
      if book.title == title:
        if book.available:
          book.available = False
          print("Book borrowed successfully")
        else:
          print("Book is already borrowed")
        return

    print("Book not found")

  def return_book(self, title):
    for book in self.books:
      if book.title == title:
        if not book.available:
          book.available = True
          print("Book returned successfully")
          return
        else:
          print("Book was not borrowed")
          return

    print("Book not found")

  def search_book(self, title):
    for book in self.books:
      if book.title == title:
        print(f"Title: {book.title}")
        print(f"Author: {book.author}")
        print(f"Published Year: {book.published_year}")
        print(f"Available: {book.available}")
        return
        
    print("Book not found")

  def create_book(self):
    title = input("Enter Title: ")
    author = input("Enter Author: ")
    year = int(input("Enter Published Year: "))

    book = Book(title, author, year)

    self.books.append(book)

    print("Book added successfully.")

  def save_books(self):
      with open("books.txt", "w") as file:
          for book in self.books:
              file.write(
                  f"{book.title}|{book.author}|{book.published_year}|{book.available}\n"
            )

  def load_books(self):
      try:
          with open("books.txt", "r") as file:
              for line in file:
                  title, author, year, available = line.strip().split("|")

                  book = Book(title, author, int(year))
                  book.available = available == "True"

                  self.books.append(book)

      except FileNotFoundError:
          pass

  def show_stats(self):
    total = len(self.books)

    borrowed = 0
    for book in self.books:
        if not book.available:
            borrowed += 1

    available = total - borrowed

    print(f"Total Books: {total}")
    print(f"Available Books: {available}")
    print(f"Borrowed Books: {borrowed}")

  def search_author(self, author):
    found = False

    for book in self.books:
        if book.author.lower() == author.lower():
            print(book.title)
            found = True

    if not found:
        print("No books found")

  


